def get_life_path_number(day, month, year):
    """–í—ã—á–∏—Å–ª—è–µ—Ç —á–∏—Å–ª–æ –∂–∏–∑–Ω–µ–Ω–Ω–æ–≥–æ –ø—É—Ç–∏"""
